SELECT 
    B_ID,
    B_Title,
    B_SUBTITLE,
    B_ISBN13,
    B_ISBN10,
    B_PUBLISHER,
    B_PUBLISH_DATE,
    B_DESCRIPTION,
    B_PAGE_COUNT,
    B_AVERAGE_RATING,
    B_RATING_COUNT,
    B_LANGUAGE,
    B_IS_PHYSICAL,
    B_AWARD_COUNT
