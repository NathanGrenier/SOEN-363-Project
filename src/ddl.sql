CREATE VIEW v_book_limited_access 
WITH (security_barrier = true, security_invoker = false)
AS
SELECT 
    B_Title,
    B_SUBTITLE,
    B_ISBN13,
    B_ISBN10,
    B_PUBLISHER,
    B_PUBLISH_DATE,
    B_DESCRIPTION,
    B_PAGE_COUNT,
    B_LANGUAGE
FROM Book
WHERE B_PUBLISH_DATE BETWEEN '2020-01-01' AND CURRENT_DATE;

